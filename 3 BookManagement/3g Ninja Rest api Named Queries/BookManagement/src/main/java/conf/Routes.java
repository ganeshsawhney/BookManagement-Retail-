package conf;


import ninja.AssetsController;
import ninja.Router;
import ninja.application.ApplicationRoutes;
import controllers.ApplicationController;

public class Routes implements ApplicationRoutes {

    @Override
    public void init(Router router) {  
        
        router.GET().route("/").with(ApplicationController::index);
        //Book Department
        router.GET().route("/book").with(ApplicationController.class, "getallBook"); // Add this line!
        router.GET().route("/book/{isbn}").with(ApplicationController.class, "getBook"); // Add this line!
        router.POST().route("/book").with(ApplicationController.class, "createBook"); // Add this line!
        router.DELETE().route("/book").with(ApplicationController.class, "delBook"); // Add this line!
        router.PUT().route("/book").with(ApplicationController.class, "editBook"); // Add this line!
           
        
 
        ///////////////////////////////////////////////////////////////////////
        // Assets (pictures / javascript)
        ///////////////////////////////////////////////////////////////////////    
        router.GET().route("/assets/webjars/{fileName: .*}").with(AssetsController::serveWebJars);
        router.GET().route("/assets/{fileName: .*}").with(AssetsController::serveStatic);
        
        ///////////////////////////////////////////////////////////////////////
        // Index / Catchall shows index page
        ///////////////////////////////////////////////////////////////////////
        router.GET().route("/.*").with(ApplicationController::index);
    }

}
